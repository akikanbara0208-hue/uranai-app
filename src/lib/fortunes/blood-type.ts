export function getBloodTypeReading(bloodType: string): FortuneResult {
  const d = DATA[bloodType] || DATA["O"];
  const todayIdx = new Date().getDate() % d.today.length;

  return {
    title: `${bloodType}型の深層分析`,
    summary: `キーワード：${d.keyword}`,
    details: [
      { label: "🧬 性格・本質", content: d.personality },
      { label: "✨ 才能・強み", content: d.strength },
      { label: "⚠️ 成長のテーマ", content: d.weakness },
      { label: "🌑 影の側面", content: d.shadow },
      { label: "❤️ 恋愛・パートナーシップ", content: d.love },
      { label: "💼 仕事・キャリア", content: d.work },
      { label: "💰 金銭・財運", content: d.money },
      { label: "🌿 健康・体のケア", content: d.health },
      { label: "😤 ストレスの原因", content: d.stress },
      { label: "🌱 成長のヒント", content: d.growth },
      { label: "💞 相性の良い血液型", content: d.compatible },
      { label: "⚡ 要注意の相性", content: d.incompatible },
      { label: "📅 今日のメッセージ", content: d.today[todayIdx] },
      { label: "📆 今週のテーマ", content: d.weeklyTheme },
      ...Object.entries(COMPATIBILITY_TABLE[bloodType] || {}).map(([bt, desc]) => ({
        label: `${bt}型との相性`,
        content: desc,
      })),
    ],
    advice: `${bloodType}型の強み「${d.strength}」を信じてください。「${d.stress}」がストレスになりやすいので、そのサインに早めに気づいて手を打つことが大切です。`,
  };
}
