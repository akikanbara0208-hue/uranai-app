import { FortuneResult } from "@/lib/types";

function rng(seed: number) {
  let s = ((seed >>> 0) || 1);
  return (max: number): number => {
    s = (Math.imul(s, 1664525) + 1013904223) >>> 0;
    return s % max;
  };
}
function pick<T>(arr: T[], r: ReturnType<typeof rng>): T {
  return arr[r(arr.length)];
}

