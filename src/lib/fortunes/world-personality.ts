export function getMBTIReading(values: Record<string, string>): FortuneResult {
  const ei = values.mbti_ei || "I";
  const sn = values.mbti_sn || "N";
  const tf = values.mbti_tf || "T";
  const jp = values.mbti_jp || "J";
  const typeCode = `${ei}${sn}${tf}${jp}`;
  const type = MBTI_TYPES[typeCode] || MBTI_TYPES["INFP"];
  const seed = typeCode.split("").reduce((a, c) => a + c.charCodeAt(0), 0);
  const r = rng(seed * 1000 + Date.now() % 10000);
  const colors = ["青", "緑", "金色", "紫", "赤", "橙", "銀", "白"];
  const stones = ["アメジスト", "ラピスラズリ", "水晶", "ローズクォーツ", "タイガーアイ", "ムーンストーン"];

  return {
    title: `MBTIタイプ：${type.name}（${type.nickname}）`,
    summary: `マイヤーズ＝ブリッグス・タイプ指標（MBTI）。4つの心理的選好から導き出される16の性格タイプのうち、あなたは「${type.name}（${type.nickname}）」です。`,
    details: [
      { label: "タイプの本質と強み", content: type.strength },
      { label: "成長への挑戦と課題", content: type.challenge },
      { label: "愛と関係性のスタイル", content: type.love },
      { label: "仕事と才能の活かし方", content: type.work },
      { label: "成長へのアドバイス", content: type.growth },
      { label: "著名な同タイプ", content: `同じ「${type.name}（${type.nickname}）」タイプとして知られる人物：${type.famous}。彼らの人生に、あなた自身のエネルギーの本質を重ねてみてください。` },
    ],
    lucky: { color: pick(colors, r), item: pick(stones, r) },
    advice: `「${type.name}」という型は、あなたを縛るラベルではなく、あなたの本質を理解するための地図です。強みを意識し、課題を知り、自分らしく生きることがMBTIから受け取れる最大のギフトです。`,
  };
}

