export function getJuniChokuReading(): FortuneResult {
  const today = new Date();
  const y = today.getFullYear(), m = today.getMonth() + 1, d = today.getDate();
  const jd0 = julianDay(y, m, d);
  const dayBranch = (jd0 + 49) % 60 % 12;
  const sunLon = getSunLongitude(jd0 - 0.5 + (12 - 9) / 24);
  const msi = Math.floor((((sunLon - 315) % 360) + 360) % 360 / 30);
  const monthBranch = (2 + msi) % 12; // 月建の十二支
  const idx = ((dayBranch - monthBranch) % 12 + 12) % 12;
  const c = CHOKU[idx];
  const weekdays = ["日", "月", "火", "水", "木", "金", "土"];

  return {
    title: `今日の十二直は「${c.name}」`,
    summary: `${y}年${m}月${d}日（${weekdays[today.getDay()]}曜日）の十二直は「${c.name}」です。${c.meaning}十二直は、その日の十二支と節気で替わる月建との関係から定まる暦注で、何をするのに向いた日かを古来の知恵で教えてくれます。`,
    details: [
      { label: "📜 十二直とは", content: "建・除・満・平・定・執・破・危・成・納・開・閉の12種からなる暦注。日の十二支と、その月の月建（節気で替わる）の関係から定まり、行動の吉凶を示します。中国から伝わり、江戸時代には暦の中段に記され、人々が日取りを選ぶ目安として親しまれてきました。" },
      { label: `📅 今日の直：${c.name}`, content: c.meaning },
      { label: "◎ 向いていること", content: c.good },
      { label: "× 控えた方がよいこと", content: c.bad },
      { label: "💼 仕事・お金の過ごし方", content: `「${c.name}」の今日は、${c.good}に通じる動きが追い風を受けます。仕事や用事は「${c.good.split("・")[0]}」のように、この直の性質に沿ったものから着手すると流れに乗れます。逆に「${c.bad}」に近いことは無理に進めず、日を改める判断も立派な戦略です。` },
      { label: "💞 人付き合い・縁", content: `${c.bad.includes("婚礼") || c.bad.includes("お祝い") ? "祝い事や大きな約束事は、慎重に運ぶほうが無難な日です。" : "人との縁を結んだり、関係を一歩進めたりするのに悪くない日です。"}「${c.name}」の気は、${c.meaning.split("。")[0]}という性質。その色合いを心に置いて人と接すると、無理のない自然な距離感で関係を築けます。` },
      { label: "🍀 今日の開運のヒント", content: `今日のキーワードは「${c.name.replace(/（.*）/, "")}」。${c.good}に類することを一つでも実行すると、暦の後押しを受けられます。朝のうちに今日やることを「向くこと」と「控えること」に仕分けしておくと、一日が驚くほど整います。` },
    ],
    advice: `暦はあくまで、追い風と向かい風を知るための目安です。「${c.name}」という今日の流れを踏まえたうえで、最後に頼りになるのはあなた自身の判断と誠実さ。日の気に逆らわず、しかし振り回されすぎず、自分のペースでこの一日を大切に過ごしてください。`,
  };
}
