import { FortuneResult } from "@/lib/types";
import { julianDay, getSunLongitude } from "@/lib/fortunes/astrology";

// ══════════════════════════════════════════════
// 十二直（じゅうにちょく）── 暦注。建除満平定執破危成納開閉。
// 日の十二支と、節気で定まる月建（その月の十二支）の関係から実算出する。
//   直 = (日支 − 月建支) mod 12   （建＝日支と月建が一致する日）
// ══════════════════════════════════════════════

