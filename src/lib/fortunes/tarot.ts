// 5枚スプレッドの各ポジションの読み解き方（カード単体の意味に、位置の役割を重ねる）
const POSITION_FRAME: Record<string, string> = {
  "現在の状況": "今のあなたの状況を映し出しているのは",
  "障害・課題": "行く手に立ちはだかる課題として現れているのは",
  "隠された影響": "表には見えていないところで働いている影響は",
  "アドバイス": "この局面を乗り越えるための指針として示されているのは",
  "結果・可能性": "このまま進んだ先に見えてくる結果・可能性は",
};

export function getTarotReading(question: string): FortuneResult {
  const q = question.split("|")[0]; // 表示用（呼び出し側が付けたタイムスタンプ部分は除く）
  const seed = question.split("").reduce((acc, c) => acc + c.charCodeAt(0), 0);
  const now = new Date();
  const dateSeed = now.getDate() + now.getMonth() * 31 + now.getFullYear();

  // 5枚スプレッド（実際のタロットと同じく、引いたカードは戻さず重複なしで選ぶ）
  const usedIdx = new Set<number>();
  const cards = SPREAD_NAMES.map((_, i) => {
    let idx = (seed + dateSeed + i * 17 + Math.floor(Math.random() * 3)) % MAJOR_ARCANA.length;
    while (usedIdx.has(idx)) idx = (idx + 1) % MAJOR_ARCANA.length;
    usedIdx.add(idx);
    const isReversed = (seed + dateSeed + i) % 4 === 0;
    return { card: MAJOR_ARCANA[idx], isReversed, position: SPREAD_NAMES[i] };
  });

  const main = cards[0];
  const advice = cards[3];

  const shortMeaning = (c: (typeof cards)[number]) => c.isReversed ? c.card.reversed : c.card.upright;
  const longMeaning = (c: (typeof cards)[number]) => c.isReversed ? c.card.reversedText : c.card.uprightText;

  // 各ポジション：カード単体の意味を、その位置の役割に沿って読み解く
  const details = cards.map((c) => ({
    label: `${c.position}：${c.card.name}（${c.isReversed ? "逆位置" : "正位置"}）`,
    content: `${POSITION_FRAME[c.position]}「${c.card.name}」──${shortMeaning(c)}。${longMeaning(c)}`,
  }));

  // 5枚を一つの物語として統合して読み解く（本来のタロットスプレッド読みの核）
  const [situation, obstacle, hidden, adviceCard, outcomeCard] = cards;
  const outcomePositive = !outcomeCard.isReversed;
  const synthesis = `「${q}」という問いに対し、5枚は一続きの物語を描いています。まず今の状況は「${situation.card.name}」──${shortMeaning(situation)}。ここに「${obstacle.card.name}」が示す「${shortMeaning(obstacle)}」という課題が重なり、その奥では「${hidden.card.name}」の「${shortMeaning(hidden)}」というエネルギーが、表からは見えないところで静かに状況を動かしています。\n\nこの状況を乗り越える鍵は、アドバイスの「${adviceCard.card.name}」が示す「${shortMeaning(adviceCard)}」という姿勢。${longMeaning(adviceCard)}\n\nそしてこの流れの先に見えてくるのは、「${outcomeCard.card.name}」が示す「${shortMeaning(outcomeCard)}」という結果・可能性です。「${situation.card.name}」から「${outcomeCard.card.name}」へ至るこの5枚の流れ全体が伝えているのは──${outcomePositive ? "行く手の課題や隠れた影響を認めつつも、アドバイスの姿勢を実践すれば、望む方向へ確かに近づいていけるということ" : "今はまだ試練の途中にあり、結果を急がず、まずはアドバイスが示す姿勢を一つずつ丁寧に実践することが遠回りに見えて一番の近道だということ"}です。`;

  // 恋愛・仕事・金運の詳細
  const mainCard = main.card;
  details.push(
    { label: "🔮 5枚から読み解く総合鑑定", content: synthesis },
    { label: "❤️ 恋愛への影響", content: main.isReversed ? mainCard.love_rev : mainCard.love_up },
    { label: "💼 仕事への影響", content: main.isReversed ? mainCard.work_rev : mainCard.work_up },
    { label: "💰 金運への影響", content: main.isReversed ? mainCard.money_rev : mainCard.money_up },
    { label: "🌿 健康へのメッセージ", content: main.isReversed ? mainCard.health_rev : mainCard.health_up },
    { label: "✨ スピリチュアルメッセージ", content: mainCard.spirit },
  );

  return {
    title: `5枚スプレッド — ${mainCard.name}${main.isReversed ? "（逆）" : ""}が中心`,
    summary: `「${q}」への答えを5枚のカードが示します`,
    details,
    drawnCards: cards.map(({ card, isReversed, position }) => ({
      position, name: card.name, symbol: TAROT_EMOJI[card.name] || "🃏", reversed: isReversed,
    })),
    lucky: mainCard.lucky,
    advice: `${advice.card.name}がアドバイスを示しています ── ${advice.isReversed ? advice.card.reversed : advice.card.upright}。${mainCard.advice}`,
  };
}
