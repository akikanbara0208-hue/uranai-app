// 本命星（1〜9）。立春(2/4頃)前は前年扱い
function honmei(birthday: string): number {
  const [y, m, d] = birthday.split("-").map(Number);
  const adj = (m === 1 || (m === 2 && d < 4)) ? y - 1 : y;
  let n = adj;
  while (n >= 10) n = String(n).split("").reduce((a, c) => a + Number(c), 0);
  let star = (11 - n) % 9;
  if (star === 0) star = 9;
  return star; // 1..9
}

