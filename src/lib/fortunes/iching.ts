export function getIChingReading(question: string): FortuneResult {
  const seed = question.split("").reduce((acc, c) => acc + c.charCodeAt(0), 0);
  const now = new Date();
  const dateSeed = now.getDate() + now.getMonth() * 31 + now.getFullYear();
  const index = (seed + dateSeed) % HEXAGRAMS.length;
  const hex = HEXAGRAMS[index];

  // 変爻（一つ上の卦）も示す
  const changingIndex = (index + 1) % HEXAGRAMS.length;
  const changing = HEXAGRAMS[changingIndex];

  return {
    title: `第${hex.number}卦 ${hex.name}`,
    summary: `${hex.symbol} ${hex.nameEn} ─ 属性：${hex.attribute}（${hex.element}）`,
    details: [
      { label: "📖 卦の全体的な意味", content: hex.overview },
      { label: "🔮 象徴する意味", content: hex.meaning },
      { label: "❤️ 恋愛・人間関係", content: hex.love },
      { label: "💼 仕事・キャリア", content: hex.work },
      { label: "💰 金運・財運", content: hex.money },
      { label: "🌿 健康・体", content: hex.health },
      { label: "✨ スピリチュアルメッセージ", content: hex.spirit },
      { label: `🔄 之卦（変化後：第${changing.number}卦 ${changing.name}）`, content: `この状況が変化すると「${changing.meaning}」の卦へと移ります。${changing.advice}` },
    ],
    advice: hex.advice,
  };
}
