function branchOf(birthday: string): number {
  const [y, m, d] = birthday.split("-").map(Number);
  const baziYear = (m === 1 || (m === 2 && d < 4)) ? y - 1 : y;
  return ((baziYear - 4) % 12 + 12) % 12;
}

