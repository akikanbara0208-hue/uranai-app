function ziweiPos(day: number, ju: number): number {
  const yu = day % ju;
  const bu = yu === 0 ? 0 : ju - yu;
  const shang = Math.ceil(day / ju);
  const pos = (shang - 1) + (bu % 2 === 0 ? bu : -bu);
  return ((2 + pos) % 12 + 12) % 12;
}

