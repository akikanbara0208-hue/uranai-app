  // 命宮の干支 → 五行局（納音簡便法）
  const lyear = (m <= 2 && lunar.month >= 11) ? y - 1 : y; // 旧暦年
  const yearStemIdx = ((lyear - 4) % 10 + 10) % 10;
  const toraStem = ((yearStemIdx % 5) * 2 + 2) % 10;        // 五虎遁：寅宮の干
  const meiStem = (toraStem + ((mei - 2) % 12 + 12) % 12) % 10;
  const stemVal = Math.floor(meiStem / 2) + 1;              // 甲乙1…壬癸5
