function getMoonPhase(y: number, m: number, d: number): number {
  // 簡易月相計算（参考年月日からの経過日数）
  const known = new Date(2000, 0, 6).getTime(); // 2000年1月6日 = 新月
  const target = new Date(y, m - 1, d).getTime();
  const days = (target - known) / 86400000;
  const cycle = 29.53058868;
  const phase = ((days % cycle) + cycle) % cycle;
  return Math.floor((phase / cycle) * 8) % 8;
}

