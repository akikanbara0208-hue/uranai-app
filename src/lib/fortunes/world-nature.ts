function bioBar(val: number): string {
  const pos = Math.round((val + 1) / 2 * 20);
  const clamped = Math.max(0, Math.min(20, pos));
  return "█".repeat(clamped) + "░".repeat(20 - clamped) + ` ${val >= 0 ? "+" : ""}${Math.round(val * 100)}%`;
}

