export function getRuneReading(question: string): FortuneResult {
  const q = question.split("|")[0]; // 表示用（呼び出し側が付けたタイムスタンプ部分は除く）
  const seed = question.split("").reduce((acc, c) => acc + c.charCodeAt(0), 0);
  const now = new Date();
  const dateSeed = now.getDate() + now.getMonth() * 31;

  // 3つのルーンを引く（過去・現在・未来。実際のルーン占いと同じく引いた石は戻さない）
  const positions = ["過去（背景・原因）", "現在（状況）", "未来（可能性・アドバイス）"];
  const usedIdx = new Set<number>();
  const runes = positions.map((_, i) => {
    let idx = (seed + dateSeed + i * 7 + Math.floor(Math.random() * 3)) % RUNES.length;
    while (usedIdx.has(idx)) idx = (idx + 1) % RUNES.length;
    usedIdx.add(idx);
    const isReversed = (seed + dateSeed + i * 3) % 5 === 0;
    return { rune: RUNES[idx], isReversed, position: positions[i] };
  });

  const main = runes[1]; // 現在のルーン
  const r = main.rune;
  const rev = main.isReversed;

  const shortMeaning = (x: (typeof runes)[number]) => x.isReversed ? x.rune.reversed : x.rune.upright;
  const longMeaning = (x: (typeof runes)[number]) => x.isReversed ? x.rune.reversedText : x.rune.uprightText;
  const FRAME: Record<string, string> = {
    "過去（背景・原因）": "今の状況の背景・原因として横たわっているのは",
    "現在（状況）": "まさに今のあなたの状況を映し出しているのは",
    "未来（可能性・アドバイス）": "この先に開けていく可能性として示されているのは",
  };

  const details = runes.map((x) => ({
    label: `${x.position}：${x.rune.symbol} ${x.rune.name}（${x.isReversed ? "逆" : "正"}）`,
    content: `${FRAME[x.position]}「${x.rune.symbol} ${x.rune.name}」──${shortMeaning(x)}。${longMeaning(x)}`,
  }));

  const [past, present, future] = runes;
  const futureIsRev = future.isReversed;
  const synthesis = `「${q}」という問いに、3つのルーンが一つの流れで答えます。背景にあるのは「${past.rune.symbol} ${past.rune.name}」──${shortMeaning(past)}。${longMeaning(past)}\n\nそれが今、「${present.rune.symbol} ${present.rune.name}」として、「${shortMeaning(present)}」という状況を形づくっています。この流れの先に見えてくるのは「${future.rune.symbol} ${future.rune.name}」が示す「${shortMeaning(future)}」という可能性です。${longMeaning(future)}\n\n「${past.rune.name}」から「${future.rune.name}」へと続くこの3つの流れ全体が伝えているのは──${futureIsRev ? "今すぐに結果を急がず、まずは現在のルーンが示す姿勢を丁寧に整えることが、遠回りに見えて確実な近道だということ" : "背景にあった経験を土台に、今この瞬間の流れに素直に乗ることで、望む未来へ着実に近づいていけるということ"}です。`;

  details.push(
    { label: "🔮 3枚から読み解く総合鑑定", content: synthesis },
    { label: `❤️ 恋愛へのメッセージ（${r.name}）`, content: rev ? r.love_rev : r.love },
    { label: `💼 仕事へのメッセージ（${r.name}）`, content: rev ? r.work_rev : r.work },
    { label: `✨ スピリチュアルメッセージ`, content: r.spirit },
    { label: `⚡ 元素とルーンの守護神`, content: `元素：${r.element} ／ 守護神：${r.deity}。${r.name}は${r.deity}の加護を帯びたルーンであり、${r.element}のエネルギーがあなたの状況に流れ込んでいます。` },
    { label: `🍀 開運のヒント`, content: `ラッキーアイテムは「${r.lucky.item}」、ラッキーカラーは「${r.lucky.color}」です。これらを身近に置くと、${r.name}のエネルギーと響き合いやすくなります。迷ったときはこのルーンの象徴を思い浮かべ、心を落ち着けてみてください。` },
  );

  return {
    title: `3ルーン・スプレッド ── ${r.symbol} ${r.name}が現在を示す`,
    summary: `「${q}」という問いに、古代北欧の知恵が3つのルーンで答えます。運命の中心に立つのは ${r.symbol} ${r.name}（${rev ? "逆位置" : "正位置"}）。${rev ? "いまは流れに逆らわず、静かに力を蓄える時かもしれません。" : "追い風が吹いています。自信を持って進むべき時です。"}`,
    details,
    drawnCards: runes.map(({ rune, isReversed, position }) => ({
      position, name: rune.name, symbol: rune.symbol, reversed: isReversed,
    })),
    lucky: r.lucky,
    advice: `${r.advice} ルーンが映し出すのは、あくまで今この瞬間に流れるエネルギーです。最後に道を選ぶのはあなた自身。この導きを参考にしながら、自分の意志でしっかりと一歩を踏み出してください。`,
  };
}
