export function getDreamReading(keyword: string): FortuneResult {
  // カンマ区切りで複数キーワードに対応
  const keys = keyword.split(",").map((k) => k.trim()).filter(Boolean);
  const matched = keys
    .map((k) => {
      const entry = Object.entries(DREAM_SYMBOLS).find(([sym]) => k.includes(sym));
      return entry ? { symbol: entry[0], data: entry[1] } : null;
    })
    .filter((x): x is { symbol: string; data: typeof DEFAULT_DREAM } => x !== null);

  if (matched.length === 0) {
    return {
      title: "夢占い結果",
      summary: DEFAULT_DREAM.overall,
      details: [
        { label: "❤️ 恋愛運", content: DEFAULT_DREAM.love },
        { label: "💼 仕事・キャリア運", content: DEFAULT_DREAM.work },
        { label: "💰 金運", content: DEFAULT_DREAM.money },
        { label: "🌿 健康運", content: DEFAULT_DREAM.health },
        { label: "🍀 ラッキーアイテム・行動", content: DEFAULT_DREAM.lucky },
      ],
      advice: DEFAULT_DREAM.advice,
    };
  }

  if (matched.length === 1) {
    const { symbol, data } = matched[0];
    return {
      title: `「${symbol}」の夢占い結果`,
      summary: data.overall,
      details: [
        { label: "❤️ 恋愛運", content: data.love },
        { label: "💼 仕事・キャリア運", content: data.work },
        { label: "💰 金運", content: data.money },
        { label: "🌿 健康運", content: data.health },
        { label: "🍀 ラッキーアイテム・行動", content: data.lucky },
      ],
      advice: data.advice,
    };
  }

  // 複数シンボルの場合：各カテゴリを最初のシンボルをベースに、他を補足として組み合わせる
  const symbols = matched.map((m) => m.symbol).join("・");
  const primary = matched[0].data;
  const rest = matched.slice(1);

  const combine = (field: keyof typeof DEFAULT_DREAM) =>
    matched.map((m) => m.data[field]).join("\n\nまた、");

  return {
    title: `「${symbols}」複合夢占い結果`,
    summary: `複数のシンボルが重なる夢を見ています。${matched.map((m) => m.data.overall).join(" さらに、")}`,
    details: [
      { label: "❤️ 恋愛運", content: combine("love") },
      { label: "💼 仕事・キャリア運", content: combine("work") },
      { label: "💰 金運", content: combine("money") },
      { label: "🌿 健康運", content: combine("health") },
      {
        label: "🍀 ラッキーアイテム・行動",
        content: rest.length > 0
          ? `【${matched[0].symbol}】${primary.lucky}\n【${rest.map((m) => m.symbol).join("・")}】${rest.map((m) => m.data.lucky).join(" / ")}`
          : primary.lucky,
      },
    ],
    advice: `${symbols}が同時に現れる夢は、複数のテーマが絡み合っているサインです。${matched.map((m) => m.data.advice).join(" そして、")}`,
  };
}

