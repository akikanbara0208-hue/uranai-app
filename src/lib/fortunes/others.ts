export function getShichuReading(birthday: string, birthHour?: number): FortuneResult {
  const [yearStr, monthStr, dayStr] = birthday.split("-");
  const year = Number(yearStr);
  const month = Number(monthStr);
  const day = Number(dayStr);

  // 太陽黄経（節入り・立春の判定に使用）。出生時刻があれば反映、なければ正午JST
  const hourForSun = birthHour !== undefined ? birthHour : 12;
  const sunJd = julianDay(year, month, day) - 0.5 + (hourForSun - 9) / 24;
  const sunLon = getSunLongitude(sunJd);

  // ── 年柱：立春（太陽黄経315°）を境に年が替わる ──
  let baziYear = year;
  if (month === 1) baziYear = year - 1;
  else if (month === 2 && sunLon < 315) baziYear = year - 1;
  const stemIdx = ((baziYear - 4) % 10 + 10) % 10;
  const branchIdx = ((baziYear - 4) % 12 + 12) % 12;
  const stem = HEAVENLY_STEMS[stemIdx];
  const branch = EARTHLY_BRANCHES[branchIdx];
  const animal = ZODIAC_ANIMALS[branchIdx];
  const element = STEM_ELEMENTS[stemIdx];
  const polarity = STEM_POLARITY[stemIdx];

  // ── 月柱：節入り（12の節気）で月支が替わる。寅月=立春315°起点 ──
  const msi = Math.floor((((sunLon - 315) % 360) + 360) % 360 / 30); // 0=寅,1=卯,...,11=丑
  const monthBranchIdx = (2 + msi) % 12;
  // 月干：五虎遁（年干から寅月の干を決め、節気ごとに+1）
  const monthStemIdx = ((stemIdx % 5) * 2 + 2 + msi) % 10;
  const monthStem = HEAVENLY_STEMS[monthStemIdx];
  const monthBranch = EARTHLY_BRANCHES[monthBranchIdx];

  // ── 日柱：ユリウス日による連続60干支（2000-01-01=戊午で校正） ──
  const dayGZ = (julianDay(year, month, day) + 49) % 60;
  const dayStemIdx = dayGZ % 10;
  const dayBranchIdx = dayGZ % 12;
  const dayStem = HEAVENLY_STEMS[dayStemIdx];
  const dayBranch = EARTHLY_BRANCHES[dayBranchIdx];

  // ── 時柱：時支は2時間刻み（子時=23〜1時）、時干は五鼠遁（日干から決定） ──
  const hourBranchIdx = birthHour !== undefined ? Math.floor((birthHour + 1) / 2) % 12 : null;
  const hourStemIdx = (birthHour !== undefined && hourBranchIdx !== null)
    ? ((dayStemIdx % 5) * 2 + hourBranchIdx) % 10
    : null;

  const animalData = ANIMAL_MEANINGS[animal] || ANIMAL_MEANINGS["龍"];
  const monthAnimal = ZODIAC_ANIMALS[monthBranchIdx];
  const dayAnimalName = ZODIAC_ANIMALS[dayBranchIdx];
  const monthAnimalData = ANIMAL_MEANINGS[monthAnimal] || ANIMAL_MEANINGS["龍"];
  const dayAnimalData = ANIMAL_MEANINGS[dayAnimalName] || ANIMAL_MEANINGS["龍"];

  const details = [
    { label: "🏛️ 年柱（命の根本）", content: `${stem}${branch}（${polarity}の${element}・${animal}）── 生まれ持った家系や社会からの土台を示す柱です。${STEM_TRAIT[stemIdx]}。この根本の気質が、人生を通じて変わらないあなたの土台になります。` },
    { label: "🌿 月柱（才能・社会運）", content: `${monthStem}${monthBranch} ── 才能や社会での立ち位置を映す柱です。${STEM_TRAIT[monthStemIdx]}。${monthAnimalData.personality.split("。")[0]}という側面が、あなたの社会的な才能として発揮されていきます。` },
    { label: "☀️ 日柱（本質・恋愛）", content: `${dayStem}${dayBranch} ── あなた自身の本質とパートナーシップを映す、四柱推命で最も重視される柱です。${STEM_TRAIT[dayStemIdx]}。${dayAnimalData.love.split("。")[0]}という愛し方の傾向が、この柱に刻まれています。` },
    ...(hourStemIdx !== null && hourBranchIdx !== null
      ? [{ label: "⏰ 時柱（晩年・子孫）", content: `${HEAVENLY_STEMS[hourStemIdx]}${EARTHLY_BRANCHES[hourBranchIdx]} ── 晩年の運勢や子孫、目上の人との縁を映す柱です。${STEM_TRAIT[hourStemIdx]}。人生の後半になるほど、この柱が示す性質が色濃く表れてきます。` }]
      : [{ label: "⏰ 時柱", content: "生まれた時間を入力するとより詳細な命式が出ます" }]),
    { label: "🐾 年支動物", content: `${animal}年生まれ ── ${animalData.personality}` },
    { label: "✨ 才能・強み", content: animalData.strength },
    { label: "🌱 成長のテーマ", content: animalData.challenge },
    { label: "❤️ 恋愛・縁", content: animalData.love },
    { label: "💼 仕事・天職", content: animalData.work },
    { label: "🔮 五行エレメント", content: `${polarity}の${element}。${element}のエネルギーを持つことで、あなたの本質が形作られています` },
    { label: "📅 今年の運勢テーマ", content: animalData.luck },
  ];

  return {
    title: `${stem}${branch}（${animal}年）の命式`,
    summary: `${year}年${month}月${day}日生まれ。四本の柱があなたの運命を示します`,
    details,
    lucky: { item: `${animal}のお守り` },
    advice: `${polarity}の${element}・${animal}年生まれのあなたへ。強みは「${animalData.strength}」。成長のテーマは「${animalData.challenge}」——この両方を意識することで、今年の運気を最大限に活かせます。`,
  };
}
