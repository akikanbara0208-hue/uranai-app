export function getBirthstoneReading(birthday: string): FortuneResult {
  const month = Number(birthday.split("-")[1]);
  const s = BIRTHSTONES.find(b => b.month === month) || BIRTHSTONES[0];
  const now = new Date();
  const currentMonth = now.getMonth() + 1;
  const birthstoneMonth = s.month;
  const isCurrentMonth = currentMonth === birthstoneMonth;

  return {
    title: `${month}月の誕生石：${s.stone}`,
    summary: `${s.color}に輝く${s.stone}（${s.originShort}産）があなたへのメッセージを持っています`,
    details: [
      { label: "💎 誕生石", content: `${month}月生まれのあなたを象徴する石は「${s.stone}」──${s.color}に輝くこの石が、生涯を通じてあなたを見守る守護石になります。` },
      { label: "🌍 原産地", content: s.origin },
      { label: "✨ 石が持つ意味", content: s.meaning },
      { label: "🔮 石のパワー・効果", content: s.power },
      { label: "❤️ 愛・人間関係への働き", content: s.love },
      { label: "💼 仕事・目標への働き", content: s.work },
      { label: "🌿 ヒーリング効果", content: s.healing },
      { label: "✋ 使い方・置き方のヒント", content: s.howToUse },
      { label: "📅 今の時期について", content: isCurrentMonth ? `今まさにあなたの誕生月！${s.stone}のエネルギーが最も強く働いています` : `誕生石はいつでもあなたの守護石。${month}月になるとエネルギーが最大化されます` },
    ],
    lucky: { color: s.color, item: s.stone },
    advice: s.advice,
  };
}

