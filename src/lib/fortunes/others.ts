export function getAngelNumberReading(number: string): FortuneResult {
  const clean = number.replace(/[^0-9]/g, "");
  const found = ANGEL_NUMBERS[clean];

  if (found) {
    return {
      title: `エンジェルナンバー ${clean}`,
      summary: found.meaning,
      details: [
        { label: "💌 天使からのメッセージ", content: found.message },
        { label: "❤️ 恋愛へのメッセージ", content: found.love },
        { label: "💼 仕事・使命へのメッセージ", content: found.work },
        { label: "✨ スピリチュアルな意味", content: found.spirit },
        { label: "📖 数字の意味", content: found.meaning },
      ],
      advice: found.advice,
    };
  }

  // 合計してシングルに
  const singleSum = clean.split("").reduce((a, n) => a + Number(n), 0);
  const singleStr = String(singleSum > 9 ? String(singleSum).split("").reduce((a, n) => a + Number(n), 0) : singleSum);
  const baseData = ANGEL_NUMBERS[singleStr] || ANGEL_NUMBERS["1"];

  return {
    title: `エンジェルナンバー ${clean}`,
    summary: `${clean}は数字${singleStr}のエネルギーを持つ特別な数字です`,
    details: [
      { label: "💌 天使からのメッセージ", content: `${clean}という特別な数字を通じて天使が語りかけています。${baseData.message}` },
      { label: "❤️ 恋愛へのメッセージ", content: baseData.love },
      { label: "💼 仕事・使命へのメッセージ", content: baseData.work },
      { label: "✨ スピリチュアルな意味", content: `${clean}の各数字（${clean.split("").join("+")}）の合計は${singleStr}。${baseData.spirit}` },
    ],
    advice: baseData.advice,
  };
}

