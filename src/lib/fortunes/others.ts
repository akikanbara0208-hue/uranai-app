// 生年を1桁に縮約して11から引く。立春(2/4)前は前年扱い
function getKyuseiStar(year: number, month: number, day: number): number {
  // 立春前（1月 or 2/1〜2/3）は前年扱い
  const adjusted = (month === 1 || (month === 2 && day < 4)) ? year - 1 : year;
  let n = adjusted;
  while (n >= 10) {
    n = String(n).split("").reduce((a, c) => a + Number(c), 0);
  }
  let star = (11 - n) % 9;
  if (star === 0) star = 9;
  return star - 1; // 0始まりインデックス
}

// 今年の流年星（立春基準）
function getCurrentYearStar(): number {
  const now = new Date();
  const y = now.getFullYear();
  const m = now.getMonth() + 1;
  const d = now.getDate();
  return getKyuseiStar(y, m, d);
}

