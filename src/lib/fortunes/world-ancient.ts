function getCelticTree(m: number, d: number) {
  const md = m * 100 + d;
  for (const t of CELTIC_TREES) {
    const [sm, sd] = t.dates[0];
    const [em, ed] = t.dates[1];
    const start = sm * 100 + sd;
    const end   = em * 100 + ed;
    if (start > end) {
      if (md >= start || md <= end) return t;
    } else {
      if (md >= start && md <= end) return t;
    }
  }
  return CELTIC_TREES[0];
}

