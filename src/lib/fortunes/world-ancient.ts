function getEgyptGod(m: number, d: number) {
  for (const g of EGYPT_GODS) {
    for (let i = 0; i < g.dates.length; i += 2) {
      const [sm, sd] = g.dates[i];
      const [em, ed] = g.dates[i + 1];
      const start = sm * 100 + sd;
      const end   = em * 100 + ed;
      const cur   = m * 100 + d;
      if (cur >= start && cur <= end) return g;
    }
  }
  return EGYPT_GODS[0];
}

