// ── ラヒリ・アヤナムシャ（回帰黄道と恒星黄道のずれ）。J2000で約23.85°、歳差で年約50.29″増 ──
function lahiriAyanamsa(jd: number): number {
  const T = (jd - 2451545.0) / 36525.0;
  return 23.85 + 1.39666 * T;
}

