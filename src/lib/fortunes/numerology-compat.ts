function reduce(n: number): number {
  if (n === 11 || n === 22 || n === 33) return n;
  while (n > 9) n = String(n).split("").reduce((a, c) => a + Number(c), 0);
  return n;
}
function lifePath(birthday: string): number {
  const [y, m, d] = birthday.split("-").map(Number);
  return reduce(String(y).split("").reduce((a, c) => a + Number(c), 0) + m + d);
}
// グループ：行動派(1,5,7) / 育成・安定(2,4,6) / 創造・拡大(3,9) / 実現(8) / マスター(11,22,33)
function group(n: number): string {
  if ([1, 5, 7].includes(n)) return "行動";
  if ([2, 4, 6].includes(n)) return "安定";
  if ([3, 9].includes(n)) return "創造";
  if (n === 8) return "実現";
  return "マスター";
}
