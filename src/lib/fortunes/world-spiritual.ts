    advice: `「${focus.name}」が今のあなたの最重要ポイントです。このチャクラが整うとき、「${focus.keywords}」のエネルギーが自然と流れ始めます。今日から「${focus.mantra}」のマントラを唱え、${focus.color}の色を意識的に取り入れることで変化が始まります。`,
